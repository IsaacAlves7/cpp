;LOOPAICB.ASM-LOOP PROGRAM USING AIC ON THE AIB. CALLS AICBCOM 
         .TITLE  "LOOPAICB"  ;LOOP USING AIC ON THE AIB
         .OPTION X           ;FOR SYMBOL XREF
         .GLOBAL RESET,BEGIN,AICSET,AICSEC,AICIO ;REF/DEF SYMBOLS
         .SECT   "VECTORS"   ;ASSEMBLE INTO VECT SECTION
RESET    .WORD   BEGIN       ;RESET VECTOR
         .DATA               ;ASSEMBLE INTO DATA SECTION
STACKS   .WORD   809F00H     ;INIT STACK POINTER DATA
AICSEC   .WORD   03H,1224H,03H,3A76H,03H,67H,0H ;    Fs=10 kHz
         .TEXT               ;ASSEMBLE INTO TEXT SECTION
BEGIN    LDP     STACKS      ;INIT DATA PAGE
         LDI     @STACKS,SP  ;SP => 809F00H
         CALL    AICSET      ;INIT AIC/TMS320C30
         LDI     0,R7        ;INIT R7 = OUTPUT TO 0
LOOP     CALL    AICIO       ;AIC I/O ROUTINE,INPUT=>R6,OUT=>R7
         LDI     R6,R7       ;OUTPUT R7=NEW INPUT SAMPLE
         BR      LOOP        ;LOOP CONTINUOUSLY
         .END                ;END



