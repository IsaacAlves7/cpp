;MULT.ASM - MULTIPLY TWO ARRAYS
         .TITLE "MULT.ASM"    ;PROGRAM TITLE
         .GLOBAL BEGIN,MULT   ;REF/DEF SYMBOLS
         .DATA                ;ASSEMBLE INTO DATA SECTION
H_ADDR   .WORD   HN           ;STARTING ADDRESS OF HN ARRAY
X_ADDR   .WORD   XN           ;STARTING ADDRESS OF XN ARRAY
HN       .FLOAT  1,2,3,4      ;ARRAY OF HN VALUES
XN       .FLOAT  2,3,4,5      ;ARRAY OF XN VALUES
         .TEXT                ;ASSEMBLE INTO TEXT SECTION
BEGIN    LDP     H_ADDR       ;INIT DATA PAGE
         LDI     @H_ADDR,AR0  ;ADDRESS OF FIRST HN VALUE->AR0
         LDI     @X_ADDR,AR1  ;ADDRESS OF FIRST XN VALUE->AR1
         LDI     3,RC         ;REPEAT COUNT REG RC=3
         CALL    MULT         ;GO TO SUBROUTINE MULT
WAIT     BR      WAIT         ;WAIT




